#ifndef __PLATFORM_ESP32S31_H__
#define __PLATFORM_ESP32S31_H__

/* Memory map */
#define ESP32S31_SRAM_BASE		0x2F000000
#define ESP32S31_SRAM_SIZE		(512 * 1024)

/* Peripheral base addresses */
#define ESP32S31_UART0_BASE		0x2038A000
#define ESP32S31_UART1_BASE		0x2038B000
#define ESP32S31_CLIC_BASE		0x10800000
#define ESP32S31_CLIC_CTRL_BASE		0x10801000
#define ESP32S31_INTR_CORE0_BASE	0x20585000
#define ESP32S31_INTR_CORE1_BASE	0x20585800
#define ESP32S31_SYSTIMER_BASE		0x20399000
#define ESP32S31_TIMERG0_BASE		0x20580000
#define ESP32S31_TIMERG1_BASE		0x20581000
#define ESP32S31_HP_SYS_CLKRST_BASE	0x20587000
#define ESP32S31_RTC_CNTL_BASE		0x20800000
#define ESP32S31_LP_SYS_BASE		0x20700000
#define ESP32S31_LP_AONCLKRST_BASE	0x20701000

/* LP system reset registers */
#define ESP32S31_LP_SYSTEM_SYS_CTRL	0x08
#define ESP32S31_LP_SYSTEM_SYS_SW_RST	(1U << 1)

/* HP CPU reset control registers */
#define ESP32S31_LP_AONCLKRST_HPCORE0_RESET_CTRL	0x3c
#define ESP32S31_LP_AONCLKRST_HPCORE1_RESET_CTRL	0x40
#define ESP32S31_LP_AONCLKRST_HPCORE_SW_RESET		(1U << 20)

/* HP system clock/reset registers */
#define ESP32S31_HP_SYS_CLKRST_SYSTIMER_CTRL0	0x120
#define ESP32S31_HP_SYS_CLKRST_SYSTIMER_APB_CLK_EN	(1U << 0)
#define ESP32S31_HP_SYS_CLKRST_SYSTIMER_RST_EN		(1U << 1)
#define ESP32S31_HP_SYS_CLKRST_SYSTIMER_FORCE_NORST	(1U << 2)
#define ESP32S31_HP_SYS_CLKRST_SYSTIMER_CLK_EN		(1U << 4)

/* CLIC parameters */
#define ESP32S31_CLIC_INTNUM		48
#define ESP32S31_CLIC_NLBITS		3
#define ESP32S31_CLIC_INTCTL_BITS	8

/* TimerG0 hw_timer[0] register offsets */
#define ESP32S31_TIMG_CONFIG		0x00
#define ESP32S31_TIMG_LO		0x04
#define ESP32S31_TIMG_HI		0x08
#define ESP32S31_TIMG_UPDATE		0x0C
#define ESP32S31_TIMG_ALARMLO		0x10
#define ESP32S31_TIMG_ALARMHI		0x14
#define ESP32S31_TIMG_INT_ENA		0x70
#define ESP32S31_TIMG_INT_RAW		0x74
#define ESP32S31_TIMG_INT_ST		0x78
#define ESP32S31_TIMG_INT_CLR		0x7C

/* Timer config register bits */
#define ESP32S31_TIMG_EN		(1U << 31)
#define ESP32S31_TIMG_INCREASE		(1U << 30)
#define ESP32S31_TIMG_ALARM_EN		(1U << 10)
#define ESP32S31_TIMG_T0_INT		(1U << 0)

/* SYSTIMER register offsets and bits */
#define ESP32S31_SYSTIMER_CONF		0x00
#define ESP32S31_SYSTIMER_UNIT0_OP	0x04
#define ESP32S31_SYSTIMER_TARGET0_HI	0x1C
#define ESP32S31_SYSTIMER_TARGET0_LO	0x20
#define ESP32S31_SYSTIMER_TARGET0_CONF	0x34
#define ESP32S31_SYSTIMER_UNIT0_VALUE_HI 0x40
#define ESP32S31_SYSTIMER_UNIT0_VALUE_LO 0x44
#define ESP32S31_SYSTIMER_COMP0_LOAD_REG 0x50
#define ESP32S31_SYSTIMER_INT_ENA	0x64
#define ESP32S31_SYSTIMER_INT_RAW	0x68
#define ESP32S31_SYSTIMER_INT_CLR	0x6C
#define ESP32S31_SYSTIMER_INT_ST	0x70
#define ESP32S31_SYSTIMER_REAL_TARGET0_LO 0x74
#define ESP32S31_SYSTIMER_REAL_TARGET0_HI 0x78

#define ESP32S31_INTR_SRC_PASS_IN_M_STATUS1 0x2DC

#define ESP32S31_SYSTIMER_TARGET0_WORK_EN	(1U << 24)
#define ESP32S31_SYSTIMER_UNIT0_WORK_EN		(1U << 30)
#define ESP32S31_SYSTIMER_CLK_EN		(1U << 31)
#define ESP32S31_SYSTIMER_UNIT0_VALUE_VALID	(1U << 29)
#define ESP32S31_SYSTIMER_UNIT0_UPDATE		(1U << 30)
#define ESP32S31_SYSTIMER_COMP0_LOAD_BIT	(1U << 0)
#define ESP32S31_SYSTIMER_TARGET0_INT		(1U << 0)
#define ESP32S31_SYSTIMER_TARGET0_PERIOD_MODE	(1U << 30)
#define ESP32S31_SYSTIMER_TARGET0_UNIT_SEL	(1U << 31)

/* UART register offsets */
#define ESP32S31_UART_FIFO		0x00
#define ESP32S31_UART_STATUS		0x1C
#define ESP32S31_UART_CLKDIV		0x14
#define ESP32S31_UART_CONF0		0x20

/* UART STATUS register fields */
#define ESP32S31_UART_TXFIFO_CNT_S	16
#define ESP32S31_UART_TXFIFO_CNT_M	(0xFF << ESP32S31_UART_TXFIFO_CNT_S)
#define ESP32S31_UART_RXFIFO_CNT_S	0
#define ESP32S31_UART_RXFIFO_CNT_M	(0xFF << ESP32S31_UART_RXFIFO_CNT_S)

/* Interrupt numbers for IPI (software interrupt via CLIC) */
#define ESP32S31_IPI_CLIC_INTNUM	3
#define ESP32S31_TIMER_CLIC_INTNUM	16

/* CPU-to-CPU interrupt sources (from interrupt matrix) */
#define ESP32S31_CPU_INTR_FROM_CPU_0	0
#define ESP32S31_CPU_INTR_FROM_CPU_1	1
#define ESP32S31_TIMERGRP0_T0_INTR_SOURCE 25
#define ESP32S31_SYSTIMER_TARGET0_INTR_SOURCE 33

/* Interrupt matrix map register fields */
#define ESP32S31_INTR_MAP_CPU_INT_MASK		0x3F
#define ESP32S31_INTR_MAP_PASS_LEVEL_SHIFT	8
#define ESP32S31_INTR_MAP_PASS_LEVEL_MASK	(0x3U << ESP32S31_INTR_MAP_PASS_LEVEL_SHIFT)
#define ESP32S31_INTR_MAP_PASS_LEVEL_S		(1U << ESP32S31_INTR_MAP_PASS_LEVEL_SHIFT)
#define ESP32S31_INTR_MAP_PASS_LEVEL_M		(2U << ESP32S31_INTR_MAP_PASS_LEVEL_SHIFT)

/* Platform configuration */
#define ESP32S31_HART_COUNT		1
#define ESP32S31_SYSTIMER_FREQ		16000000
#define ESP32S31_TIME_CSR_FREQ		40000000
#define ESP32S31_TIMER_FREQ		ESP32S31_TIME_CSR_FREQ
#define ESP32S31_UART_BAUDRATE		115200
#define ESP32S31_UART_INPUT_FREQ	80000000

#endif /* __PLATFORM_ESP32S31_H__ */
